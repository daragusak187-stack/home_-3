point = int(input("Введіть Ваш бал з екзаменів(0-100):"))

if point <0:
    print("Помилка! бали повинні бути від 0 до 100!!!😡")
elif point <= 49:
    print("Ваша оцінка - незадовільна😢")
    print("Потрібно підтягнути знання!!!📘")
elif point <= 69:
    print("Ваша оцінка - задовільно😊")
    print("Є прогрес! Вчись далі.")
elif point <= 89:
    print("Ваша оцінка - добре😏")
    print("Молодець, так тримати!")
else:
    print("Ваша оцінка - відмінно🥳")
    print("Ти суперстар! 🚀")